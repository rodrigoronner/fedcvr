"""
experiments/run_statistical_tests.py
=====================================
Computes the paired statistical tests reported in the paper (Table 5)
from the fold-level results generated by run_cross_validation.py.

Method (Section 3.4 of the paper)
---------------------------------
Paired t-tests across the 5 cross-validation folds (each fold = one
held-out institution evaluated under both strategies, same seed), with
Bonferroni correction for the family of comparisons.

When multiple seeds are available, fold results are averaged per
(strategy, held-out client) across seeds before pairing, increasing the
stability of the estimates.

Usage
-----
    python -m experiments.run_statistical_tests \
        --cv_csv results/cv_fold_results.csv \
        --out_csv results/statistical_tests.csv
"""

from __future__ import annotations

import argparse
import itertools

import numpy as np
import pandas as pd
from scipy import stats


METRICS = ["f1_score", "auc", "accuracy", "recall"]

# Pairwise comparisons reported in the paper
COMPARISONS = [
    ("FedCVR-NoDP", "FedAvg"),
    ("FedCVR-Complete", "FedAvg"),
    ("FedCVR-NoDP", "FedCVR-Complete"),
]


def main(cv_csv: str, out_csv: str, alpha: float = 0.05) -> None:
    df = pd.read_csv(cv_csv)

    # Average across seeds per (strategy, held-out client)
    grouped = (
        df.groupby(["strategy", "held_out_client"])[METRICS]
        .mean()
        .reset_index()
    )

    rows = []
    n_tests = len(COMPARISONS) * len(METRICS)
    bonferroni_alpha = alpha / n_tests

    for (strat_a, strat_b), metric in itertools.product(COMPARISONS, METRICS):
        a = grouped[grouped["strategy"] == strat_a].set_index("held_out_client")[metric]
        b = grouped[grouped["strategy"] == strat_b].set_index("held_out_client")[metric]
        common = a.index.intersection(b.index)
        if len(common) < 2:
            continue
        a, b = a.loc[common], b.loc[common]

        t_stat, p_value = stats.ttest_rel(a.values, b.values)
        mean_diff = float(np.mean(a.values - b.values))
        significant = p_value < bonferroni_alpha

        rows.append(
            {
                "comparison": f"{strat_a} vs {strat_b}",
                "metric": metric,
                "n_folds": len(common),
                "mean_diff": round(mean_diff, 4),
                "t_statistic": round(float(t_stat), 3),
                "p_value": round(float(p_value), 4),
                "bonferroni_alpha": round(bonferroni_alpha, 4),
                "significant": significant,
            }
        )

    out = pd.DataFrame(rows)
    out.to_csv(out_csv, index=False)
    print(out.to_string(index=False))
    print(f"\nSaved to {out_csv}")
    print(
        f"Bonferroni-corrected alpha = {alpha}/{n_tests} = "
        f"{bonferroni_alpha:.4f}"
    )
    print(
        "Use these values, and ONLY these values, to fill the statistical "
        "significance table in the paper."
    )


if __name__ == "__main__":
    parser = argparse.ArgumentParser(
        description="FedCVR – paired t-tests with Bonferroni correction"
    )
    parser.add_argument("--cv_csv", type=str, default="results/cv_fold_results.csv")
    parser.add_argument("--out_csv", type=str, default="results/statistical_tests.csv")
    parser.add_argument("--alpha", type=float, default=0.05)
    args = parser.parse_args()

    main(args.cv_csv, args.out_csv, args.alpha)
